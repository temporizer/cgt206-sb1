<p>As an individual seeking to get a Tattoo, you may want to ask yourself some key questions: 
<ul>
	<li>
Are the needles reused and sterilized or are they new ones each time?
	</li>
	<li>
This can cause cross contamination, if not done properly. 
	</li>
	<li>
Does it look like the proper hygienic guidelines are followed?
	</li>
	<li>
If not, it may cause illnesses like hepatitis B and C, and  HIV to name a few. 
	</li>
	<li>
Is the dye made from metal?
	</li>
	<li>
It may cause allergic reaction, which if not remedied the parts in the tattoo will have to be removed. 
	</li>
</ul>
</p>

<p>Now if you are wanting to be a walking canvas of art, please be sure that the establishment is a clean and reputable one.  </p>
<p>Read up on after-care instructions, so you have a better understanding before you get the Tattoo. Know that it takes 2 weeks to heal and exposing it to sunlight can damage the quality of the tattoo.  </p>
